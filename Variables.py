#cualquier tipo de dato puede ser guardado en una varibale como se muestar acontinuacion
name = "Eduardo"
print (name)
x = 100
print (x)
#case sensitive
#es cuando las diferencua una mayuscula o un cambio minimo como se muestra
#eso se hace para no poner numeros en una variable ya que no es valido/correcto
Nombre = "Eduardito"
nombre = "Lalito"
print (Nombre)
print (nombre)
#tambien se pueden combinar variables por separado como se muestra
x, libro = 10, "Prueba para ver"
print (x)
print (libro)
#Conventiones
#es cuando se va diferenciando la misma palabra por simbolos o mayusculas para dar otro valor
NombreMio = "poder"  #pascal case
nombreMio = "Programacion" #Camel case
Nombre_Mio = "Diferencia" #Snake case
#Constant
#es un valor que no cambia como por ejemplo el valor de pi
pi = 3.1416
MiNombre = "Eduardo"
print (pi)
print (MiNombre)

